// Bootstrap step run by the root `prepare` script after `pnpm install`.
// Creates the gitignored `version.gen.ts` files with dev-placeholder
// values if they don't already exist, so a fresh checkout can run
// typecheck / test / build without first running the deploy stamper.
//
//   apps/web/src/version.gen.ts        — KINDOO_WEB_VERSION='0.0.0-dev',
//                                        KINDOO_WEB_BUILT_AT='dev'
//   functions/src/version.gen.ts       — KINDOO_FUNCTIONS_VERSION='0.0.0-dev',
//                                        KINDOO_FUNCTIONS_BUILT_AT='dev'
//
// Idempotent: skips any file that already exists. The deploy stamper
// (`stamp-version.js`) overwrites these unconditionally, so an existing
// stamped value is preserved across `pnpm install` runs.
//
// Pure Node, no shell-quoting gymnastics; tolerates running before the
// per-workspace src/ directories exist (skips silently in that case).

const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..', '..');

const targets = [
  {
    file: path.join(repoRoot, 'apps', 'web', 'src', 'version.gen.ts'),
    prefix: 'KINDOO_WEB',
  },
  {
    file: path.join(repoRoot, 'functions', 'src', 'version.gen.ts'),
    prefix: 'KINDOO_FUNCTIONS',
  },
];

const banner =
  '// Auto-generated by infra/scripts/ensure-version-gen.js (placeholder)\n' +
  '// or infra/scripts/stamp-version.js (deploy stamp). Gitignored — do\n' +
  '// not commit. Re-create by running `node infra/scripts/ensure-version-gen.js`.\n';

function bodyFor(prefix) {
  return (
    banner +
    'export const ' +
    prefix +
    "_VERSION = '0.0.0-dev';\n" +
    'export const ' +
    prefix +
    "_BUILT_AT = 'dev';\n"
  );
}

let created = 0;
let kept = 0;
let skipped = 0;

for (const target of targets) {
  const dir = path.dirname(target.file);
  if (!fs.existsSync(dir)) {
    skipped++;
    continue;
  }
  if (fs.existsSync(target.file)) {
    kept++;
    continue;
  }
  fs.writeFileSync(target.file, bodyFor(target.prefix), 'utf8');
  created++;
}

console.log(
  'ensure-version-gen: created ' +
    created +
    ' file(s), kept ' +
    kept +
    ' existing, skipped ' +
    skipped +
    ' missing dir(s)',
);
